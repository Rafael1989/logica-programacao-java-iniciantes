
public class TiposNumericos {
	
	public static void main(String[] args) {
		Byte numeroByte = 127; // -128 ate 127
		
		Short numeroShort = 32767; // -32768 ate 32767
		
		Integer numeroInteger = 2147483647; // -2147483648 ate 2147483647
		
		Long numeroLong = 9223372036854775807L; // −9223372036854775808 ate 9223372036854775807
		
		Float numeroFloat = 1.0f; // 1.17549435×10^38 ate 3.40282347×10^38
		
		Double numeroDouble = 1.0; // 2.2250738585072014×10^308 ate 1.7976931348623157×10^308
		
	}

}
